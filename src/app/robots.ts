import { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
    const siteUrl = process.env.NEXT_PUBLIC_SITE_URL || "https://cesiha.com";

    return {
        rules: [
            {
                userAgent: "*",
                allow: "/",
                disallow: ["/admin/", "/login/", "/auth/"],
            },
        ],
        sitemap: `${siteUrl}/sitemap.xml`,
    };
}
